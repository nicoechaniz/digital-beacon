def main(argv=None):
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("inputs", nargs="*", type=Path,
                        help="Specific WAV files (default: scan voice dir)")
    args = parser.parse_args(argv)

    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s | %(levelname)s | %(message)s",
        datefmt="%H:%M:%S",
    )

    if args.inputs:
        inputs = args.inputs
        log.info("Using %d specified inputs", len(inputs))
    else:
        inputs = discover_inputs(VOICE_DIR)
    if not inputs:
        log.error("No usable voice samples found in %s", VOICE_DIR)
        sys.exit(1)

    samples = [process_one(p) for p in inputs]
    log.info("=" * 60)
    log.info("Processed %d samples", len(samples))

    html = build_html(samples)
    HTML_PATH.write_text(html)
    log.info("Wrote %s (%d KB)", HTML_PATH, HTML_PATH.stat().st_size // 1024)

    # Drop a manifest JSON alongside for debugging
    manifest = OUT_DIR / "manifest.json"
    manifest.write_text(json.dumps(samples, indent=2))
    log.info("Wrote %s", manifest)


if __name__ == "__main__":
    sys.exit(main())